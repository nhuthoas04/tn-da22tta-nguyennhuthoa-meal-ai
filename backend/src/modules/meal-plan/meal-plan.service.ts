  private scoreMealPlanItemForKeeping(
    item: MealPlanItem,
    dayItems: MealPlanItem[],
  ): number {
    const recipe = item.recipe;
    if (!recipe) return 0;

    let score = 0;

    // 1. MÃ³n chÃ­nh vs MÃ³n phá»¥
    const tags = recipe.tags || [];
    const isMain = !tags.includes('canh') && !tags.includes('rau');
    if (isMain) {
      score += 50;
    }
    if (item.notes === 'MÃ³n phá»¥') {
      score -= 20;
    }

    // 2. MÃ³n cÃ³ calories/protein tá»‘t hÆ¡n
    const protein = Number(recipe.protein) || 0;
    const calories = Number(recipe.calories || item.calories) || 0;
    score += protein * 2;
    if (calories >= 250 && calories <= 800) {
      score += 15;
    }

    // 3. MÃ³n Ã­t bá»‹ trÃ¹ng vs MÃ³n bá»‹ láº·p
    const recipeOccurrences = dayItems.filter(
      (i) => i.recipeId === recipe.id,
    ).length;
    if (recipeOccurrences > 1) {
      score -= 100; // Deduct heavily for duplicate recipes
    }

    // 4. MÃ³n phÃ¹ há»£p bá»¯a Äƒn hÆ¡n
    if (recipe.mealType && recipe.mealType.includes(item.mealType)) {
      score += 30;
    }

    return score;
  }

  private pickBestMealItems(
    items: MealPlanItem[],
    limit: number,
    keepIds: Set<string>,
    dayItems: MealPlanItem[],
  ) {
    return items
      .filter((item) => !keepIds.has(item.id))
      .sort((a, b) => {
        if (a.isLocked !== b.isLocked) return a.isLocked ? -1 : 1;

        const scoreA = this.scoreMealPlanItemForKeeping(a, dayItems);
        const scoreB = this.scoreMealPlanItemForKeeping(b, dayItems);
        if (scoreA !== scoreB) {
          return scoreB - scoreA;
        }

        // Keep older item first (MÃ³n thÃªm sau cÃ¹ng bá»‹ loáº¡i)
        const timeA = new Date(a.createdAt || 0).getTime();
        const timeB = new Date(b.createdAt || 0).getTime();
        return timeA - timeB;
      })
      .slice(0, Math.max(0, limit));
  }

  private async optimizeMealPlanItemsForPortions(
    planId: string,
    dates: Date[],
    servings: number,
    dailyCalorieTarget?: number,
    mealTypesToOptimize: string[] = this.MEAL_TYPES,
  ): Promise<{
    optimized: boolean;
    beforeCount: number;
    afterCount: number;
    removedItems: { recipeName: string; mealType: string; dayOfWeek: number }[];
    errorReason: string | null;
  }> {
    const dateStrings = Array.from(
      new Set(dates.map((date) => this.formatDateInput(date))),
    );
    if (dateStrings.length === 0) {
      return {
        optimized: false,
        beforeCount: 0,
        afterCount: 0,
        removedItems: [],
        errorReason: null,
      };
    }

    const allItems = await this.itemRepo.find({
      where: {
        mealPlanId: planId,
        mealDate: In(dateStrings),
      },
      relations: ['recipe'],
    });

    const recipeCount = await this.recipeRepo.count();
    if (recipeCount === 0) {
      return {
        optimized: false,
        beforeCount: allItems.length,
        afterCount: allItems.length,
        removedItems: [],
        errorReason: 'not_enough_recipes',
      };
    }

    const idsToRemove: string[] = [];
    const removedItemsDetails: {
      recipeName: string;
      mealType: string;
      dayOfWeek: number;
    }[] = [];
    let beforeCount = allItems.filter((item) => item.recipeId).length;
    let afterCount = beforeCount;
    let errorReason: string | null = null;
    let optimized = false;

    for (const dateStr of dateStrings) {
      const dayItems = allItems.filter(
        (item) =>
          this.formatDateInput(new Date(item.mealDate)) === dateStr &&
          item.recipeId,
      );

      beforeCount = dayItems.length;

      const activeMealTypes = mealTypesToOptimize.filter(
        (mealType) => !this.isMealSlotInPast(new Date(dateStr), mealType),
      );
      const protectedItems = dayItems.filter(
        (item) => !activeMealTypes.includes(item.mealType),
      );
      const optimizableItems = dayItems.filter((item) =>
        activeMealTypes.includes(item.mealType),
      );

      if (activeMealTypes.length === 0 || optimizableItems.length === 0) {
        afterCount = dayItems.length;
        continue;
      }

      const keepIds = new Set<string>();
      protectedItems.forEach((item) => keepIds.add(item.id));

      for (const mealType of activeMealTypes) {
        const mealItems = optimizableItems.filter((item) => item.mealType === mealType);
        const targetForMeal = this.getTargetDishesCount(servings, mealType);
        const lockedItems = mealItems.filter((item) => item.isLocked);
        const lockedInMeal = lockedItems.length;
        if (lockedInMeal > targetForMeal) {
          errorReason = `locked_exceeds_limit:${lockedInMeal}:${targetForMeal}`;
          afterCount = dayItems.length;
          break;
        }
        lockedItems.forEach((item) => keepIds.add(item.id));
        const needMore = Math.max(0, targetForMeal - lockedInMeal);

        this.pickBestMealItems(mealItems, needMore, keepIds, dayItems).forEach(
          (item) => keepIds.add(item.id),
        );
      }

      if (errorReason) break;

      // Removed nutrition calorie limit check

      const dayToRemove = optimizableItems.filter(
        (item) => !item.isLocked && !keepIds.has(item.id),
      );
      for (const item of dayToRemove) {
        idsToRemove.push(item.id);
        removedItemsDetails.push({
          recipeName: item.recipe?.name || 'MÃ³n Äƒn khÃ´ng tÃªn',
          mealType: item.mealType,
          dayOfWeek: this.getDayOfWeekIndex(item.mealDate),
        });
      }

      if (dayToRemove.length > 0) {
        optimized = true;
      }
      afterCount = dayItems.length - dayToRemove.length;
    }

    if (idsToRemove.length > 0 && !errorReason) {
      await this.itemRepo.delete({ id: In(idsToRemove) });
    } else {
      optimized = false;
    }

    return {
      optimized,
      beforeCount,
      afterCount,
      removedItems: removedItemsDetails,
      errorReason,
    };
  }

  private getTargetDishesCount(servings: number, mealType: string): number {
    const s = Math.max(1, Math.floor(Number(servings) || 1));
    if (s <= 1) {
      return 2;
    }
    if (s <= 2) {
      if (mealType === 'breakfast' || mealType === 'snack') return 2;
      return 3;
    }
    if (s <= 4) {
      if (mealType === 'breakfast' || mealType === 'snack') return 3;
      return 4;
    }
    if (s <= 6) {
      if (mealType === 'breakfast' || mealType === 'snack') return 4;
      return 5;
    }
    // Fallback for > 6 servings
    const max = getMaxRecommendedDishes(s);
    const bf = Math.max(4, Math.round(max * 0.28));
    const ld = Math.max(1, Math.round((max - bf) / 2));
    if (mealType === 'breakfast' || mealType === 'snack') return bf;
    if (mealType === 'lunch') return ld;
    return max - bf - ld; // dinner
  }

