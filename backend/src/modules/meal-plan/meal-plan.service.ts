  /**
   * Get meal plan for a specific week
   */
  async findByWeek(userId: string, weekStart?: string, warning?: string) {
    const startDate = weekStart
      ? this.parseDateInput(weekStart)
      : this.getMonday(new Date());

    const plan = await this.planRepo.findOne({
      where: { userId, weekStart: startDate },
      relations: ['items', 'items.recipe'],
    });

    if (!plan) return null;

    // Build response with day labels and nutrition data
    const items = plan.items.map((item) => ({
      id: item.id,
      dayOfWeek: this.getDayOfWeekIndex(item.mealDate),
      dayLabel: this.DAY_LABELS[this.getDayOfWeekIndex(item.mealDate)],
      mealDate:
        item.mealDate instanceof Date
          ? this.formatDateInput(item.mealDate)
          : item.mealDate,
      mealType: item.mealType,
      recipe: item.recipe
        ? {
          id: item.recipe.id,
          name: item.recipe.name,
          imageUrl: item.recipe.imageUrl,
          calories: item.recipe.calories,
          protein: Number(item.recipe.protein) || 0,
          carbs: Number(item.recipe.carbs) || 0,
          fat: Number(item.recipe.fat) || 0,
          cookingTime: item.recipe.cookingTime,
        }
        : null,
      calories: item.calories,
      isLocked: item.isLocked,
      isConsumed: item.isConsumed,
    }));

    // Calculate daily nutrition
    const nutrition = this.calculateNutrition(plan.items);

    return {
      id: plan.id,
      weekStart: plan.weekStart,
      weekEnd: plan.weekEnd,
      isAutoGenerated: plan.isAutoGenerated,
      status: plan.status,
      totalCalories: plan.totalCalories,
      dailyAvgCalories: Math.round((plan.totalCalories || 0) / 7),
      items,
      nutrition,
      warning,
    };
  }

