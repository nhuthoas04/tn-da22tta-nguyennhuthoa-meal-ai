  async getNutrition(userId: string, planId: string) {
    const plan = await this.planRepo.findOne({
      where: { id: planId, userId },
      relations: ['items', 'items.recipe'],
    });
    if (!plan) throw new NotFoundException('Meal plan not found');

      const user = await this.userRepo.findOne({ where: { id: userId } });
      const daily = this.calculateNutrition(plan.items);
      const totalDishes = daily.reduce((sum, d) => sum + d.dishCount, 0);
      const dataDays = daily.filter((day) => day.dishCount > 0).length;
      const averageDivisor = dataDays || 1;
      const incompleteNutritionCount = plan.items.filter((item) => {
        if (!item.recipe) return false;
        const calories = Number(item.recipe.calories) || 0;
        const protein = Number(item.recipe.protein) || 0;
        const carbs = Number(item.recipe.carbs) || 0;
        const fat = Number(item.recipe.fat) || 0;
        return calories <= 0 || (protein === 0 && carbs === 0 && fat === 0);
      }).length;

      // Average only across days that actually contain a menu.
      const weeklyAvg = {
        calories: Math.round(
          daily.reduce((sum, d) => sum + d.calories, 0) / averageDivisor,
        ),
        protein:
          Math.round(
            (daily.reduce((sum, d) => sum + d.protein, 0) / averageDivisor) *
              10,
          ) /
          10,
        carbs:
          Math.round(
            (daily.reduce((sum, d) => sum + d.carbs, 0) / averageDivisor) * 10,
          ) / 10,
        fat:
          Math.round(
            (daily.reduce((sum, d) => sum + d.fat, 0) / averageDivisor) * 10,
          ) / 10,
    };

    // Macro distribution percentages
    const totalMacroCalories =
      weeklyAvg.protein * 4 + weeklyAvg.carbs * 4 + weeklyAvg.fat * 9;
    const macroDistribution = {
      proteinPercent:
        totalMacroCalories > 0
          ? Math.round(
            ((weeklyAvg.protein * 4) / totalMacroCalories) * 100 * 10,
          ) / 10
          : 0,
      carbsPercent:
        totalMacroCalories > 0
          ? Math.round(
            ((weeklyAvg.carbs * 4) / totalMacroCalories) * 100 * 10,
          ) / 10
          : 0,
      fatPercent:
        totalMacroCalories > 0
          ? Math.round(((weeklyAvg.fat * 9) / totalMacroCalories) * 100 * 10) /
          10
          : 0,
    };

    return {
      calorieTarget: user?.dailyCalorieTarget || 0,
      daily,
      weeklyAvg,
        macroDistribution,
        totalDishes,
        dataDays,
        incompleteNutritionCount,
      };
  }

  private calculateNutrition(items: MealPlanItem[]) {
    const dailyMap = new Map<
      number,
      {
        calories: number;
        protein: number;
        carbs: number;
        fat: number;
        dishCount: number;
      }
    >();

    for (let d = 1; d <= 7; d++) {
      dailyMap.set(d, {
        calories: 0,
        protein: 0,
        carbs: 0,
        fat: 0,
        dishCount: 0,
      });
    }

    for (const item of items) {
      if (item.recipe) {
        const dayOfWeek = this.getDayOfWeekIndex(item.mealDate);
        const day = dailyMap.get(dayOfWeek);
        if (day) {
          day.calories += item.recipe.calories || 0;
          day.protein += Number(item.recipe.protein) || 0;
          day.carbs += Number(item.recipe.carbs) || 0;
          day.fat += Number(item.recipe.fat) || 0;
          day.dishCount += 1;
        }
      }
    }

    return Array.from(dailyMap.entries()).map(([day, data]) => ({
      day,
      label: this.DAY_LABELS[day],
      ...data,
    }));
  }

